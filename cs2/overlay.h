void DrawString(float fontSize, int x, int y, ImVec4 color, bool bCenter, bool stroke, const char* pText, ...)
{
	va_list va_alist;
	char buf[1024] = { 0 };
	va_start(va_alist, pText);
	_vsnprintf_s(buf, sizeof(buf), pText, va_alist);
	va_end(va_alist);
	std::string text = buf;
	if (bCenter)
	{
		float offsetX = 0.0f;
		float offsetY = 0.0f;

		ImFont* font = ImGui::GetFont();
		ImVec2 textSize = font->CalcTextSizeA(fontSize, FLT_MAX, 0.0f, text.c_str());
		offsetX = textSize.x * 0.5f;
		offsetY = textSize.y;
		x -= offsetX;
		y -= offsetY;
	}
	ImDrawList* drawList = ImGui::GetBackgroundDrawList();
	if (stroke)
	{
		ImVec4 shadowColor(0, 0, 0, 1);
		drawList->AddText(ImGui::GetFont(), fontSize, ImVec2(x + 0.6f, y + 0.6f), ImGui::ColorConvertFloat4ToU32(shadowColor), text.c_str());
		drawList->AddText(ImGui::GetFont(), fontSize, ImVec2(x - 0.6f, y - 0.6f), ImGui::ColorConvertFloat4ToU32(shadowColor), text.c_str());
		drawList->AddText(ImGui::GetFont(), fontSize, ImVec2(x + 0.6f, y - 0.6f), ImGui::ColorConvertFloat4ToU32(shadowColor), text.c_str());
		drawList->AddText(ImGui::GetFont(), fontSize, ImVec2(x - 0.6f, y + 0.6f), ImGui::ColorConvertFloat4ToU32(shadowColor), text.c_str());
	}
	drawList->AddText(ImGui::GetFont(), fontSize, ImVec2(x, y), ImGui::ColorConvertFloat4ToU32(color), text.c_str());
}

